function get_product($category_id) {
    global $db;
    $query = 'SELECT *
              FROM products
              WHERE category_id = :category_id
             ';
    $statement = $db->prepare($query);
    $statement->bindValue(':category_id', $category_id);
    $statement->execute();
    $products = $statement->fetchAll();
    $statement->closeCursor();
    return $products;
}